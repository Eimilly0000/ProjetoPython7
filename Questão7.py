#Declarar

Largura : int = 0
Altura : int = 0
Comprimento : int = 0
Volume : int = 0

#Inicio

Largura = int (input("Digite o valor da Largura do Paralelepípedo: "))
Altura = int (input("Digite o valor da Altura Paralelepípedo: "))
Comprimento = int (input ("Digite o valor do Comprimento Paralelepípedo: "))
Volume = (Comprimento * Largura * Altura)
print ("O Volume do seu Paralelepípedo é: ",Volume)

#Fim